        {
            this.ChangePowerAnalyzerStart(false);
        }

        private void MainForm_Load(object sender, EventArgs e)
        {
            this.RefreshDefaultData();
            this.SetupPowerAnalyzer(false);
            if (!base.DesignMode)
            {
                this.currentPowerReadingBindingSource.DataSource = this.powerReading;
                this.pipe = new Receiver<AnalyzerRequest>();
                this.pipe.Data = (PipeService<AnalyzerRequest>.DataIsReady) Delegate.Combine(this.pipe.Data, new PipeService<AnalyzerRequest>.DataIsReady(this.DataReceived));
                if (!this.pipe.ServiceOn())
                {
                    MessageBox.Show(this.pipe.Error.Message);
                }
            }
        }

        private void powerAnalyzer_ReadingsUpdated(decimal[] readings)
        {
            this.InvokeSafe<MainForm>(delegate (MainForm form) {
                try
                {
                    readings[4] = this.CalculateOhms(readings);
                    if (readings[6] == 0M)
                    {
                        this.powerReading.Amps = 0M;
                        this.powerReading.Volts = 0M;
                        this.powerReading.PowerFactor = 0M;
                        this.powerReading.Watts = 0M;
                        this.powerReading.Ohms = 0M;
                    }
                    else
                    {
                        this.powerReading.Amps = readings[3];
                        this.powerReading.Volts = readings[2];
                        this.powerReading.PowerFactor = readings[5];
                        this.powerReading.Watts = readings[1];
                        this.powerReading.Ohms = readings[4];
                    }
                }
                catch (Exception ex)
                {
                    MessageBox.Show("powerAnalyzer_ReadingsUpdated - " + ex.Message + " - " + ex.StackTrace);
                }
            });
        }

        private void powerAnalyzer_RunStateChanged(RunState state)
        {
            base.Invoke((Action)delegate {
                switch (state)
                {
                    case RunState.Started:
                        this.bsiStatus.Caption = "Power Analyzer Started...";
                        return;

                    case RunState.Stopped:
                        this.bsiStatus.Caption = "Power Analyzer Started...";
                        return;
                }
            });
        }

        private void RefreshDefaultData()
        {
//            IPowerAnalyzerRepository repository = new PowerAnalyzerRepository();
            PowerAnalyzerRepository repository = new PowerAnalyzerRepository(); 
            this.doorCountBindingSource.DataSource = repository.GetDoorCounts();
            this.modelBindingSource.DataSource = repository.GetModels();
            this.lineBindingSource.DataSource = repository.GetLines();
            this.frameTypeBindingSource.DataSource = repository.GetFrameTypes();
            this.voltageBindingSource.DataSource = repository.GetVoltages();
            this.powerTableReferenceBindingSource.DataSource = typeof(PowerTable);
            this.RefreshReferenceData();
        }

