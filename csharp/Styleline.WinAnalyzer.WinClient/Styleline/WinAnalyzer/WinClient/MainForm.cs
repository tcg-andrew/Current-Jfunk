        protected bool LookupPowerTableReference()
        {
            bool flag = false;
            this.powerTableReferenceBindingSource.DataSource = typeof(PowerTable);
            if (flag = this.cbxDoorCount.EditValue == null)
            {
                MessageBox.Show("You must select a qty of doors.", "Invalid Input", MessageBoxButtons.OK, MessageBoxIcon.Hand);
            }
            if (flag = this.cbxModel.EditValue == null)
            {
                MessageBox.Show("You must select a model.", "Invalid Input", MessageBoxButtons.OK, MessageBoxIcon.Hand);
            }
            if (flag = this.cbxLine.EditValue == null)
            {
                MessageBox.Show("You must select a line.", "Invalid Input", MessageBoxButtons.OK, MessageBoxIcon.Hand);
            }
            if (flag = this.cbxFrame.EditValue == null)
            {
                MessageBox.Show("You must select a frame type.", "Invalid Input", MessageBoxButtons.OK, MessageBoxIcon.Hand);
            }
            if (flag = this.cbxVoltage.EditValue == null)
            {
                MessageBox.Show("You must select a voltage.", "Invalid Input", MessageBoxButtons.OK, MessageBoxIcon.Hand);
            }
            if (flag)
            {
                return false;
            }
            int editValue = (int) this.cbxDoorCount.EditValue;
            int modelId = (int) this.cbxModel.EditValue;
            int lineId = (int) this.cbxLine.EditValue;
            int frameTypeId = (int) this.cbxFrame.EditValue;
            int voltageId = (int) this.cbxVoltage.EditValue;
            IPowerAnalyzerRepository repository = new PowerAnalyzerRepository();
            PowerTable table = repository.GetPowerTable(editValue, modelId, lineId, frameTypeId, voltageId);
            if (table == null)
            {
                MessageBox.Show("Unable to find an electrical table for that criteria.", "Not Found", MessageBoxButtons.OK, MessageBoxIcon.Exclamation);
                return false;
            }
            this.powerTableReferenceBindingSource.DataSource = table;
            return true;
        }

