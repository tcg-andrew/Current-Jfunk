        private void btnRecordDoorLine_Click(object sender, EventArgs e)
        {
            try
            {
                if (this.powerTableReferenceBindingSource.Current != null)
                {
                    PowerTable current = this.powerTableReferenceBindingSource.Current as PowerTable;
                    if ((this.powerAnalyzer == null) || (this.powerAnalyzer.RunState == RunState.Stopped))
                    {
                        XtraMessageBox.Show("Power Analyzer is not started.");
                    }
                    else
                    {
                        decimal num;
                        decimal num2;
                        if ((decimal.TryParse(current.drdrohlo, out num) && decimal.TryParse(current.drfrohhi, out num2)) && (((num <= this.powerReading.Ohms) && (this.powerReading.Ohms <= num2)) || (XtraMessageBox.Show("Readings are outside the range." + Environment.NewLine + "Would you like to take the recording anyways?", "Outside Range", MessageBoxButtons.YesNo, MessageBoxIcon.Exclamation, MessageBoxDefaultButton.Button2) == DialogResult.Yes)))
                        {
                            AnalyzerRequest currentRequest = this.CurrentRequest;
                            AnalyzerResponse callback = new AnalyzerResponse
                            {
                                Request = currentRequest,
                                Amps = this.powerReading.Amps,
                                Ohms = this.powerReading.Ohms,
                                PF = this.powerReading.PowerFactor,
                                Volts = this.powerReading.Volts,
                                Watts = this.powerReading.Watts,
                                OhmsHigh = num2,
                                OhmsLow = num,
                                PowerTableId = current.Id
                            };
                            int? maxRecording = null;
                            IPowerAnalyzerRepository repository = new PowerAnalyzerRepository();
                            callback.RecordedId = repository.InsertReading(current.Id, currentRequest.JobNumber, currentRequest.AssemblyNumber, 'D', "", callback.Volts, callback.Watts, callback.PF, callback.Ohms, callback.Amps, true, ref maxRecording);
                            int? nullable2 = maxRecording;
                            callback.RecordedCount = nullable2.HasValue ? nullable2.GetValueOrDefault() : 0;
                            callback.Revision = current.Revision.RevisionName;
                            this.pipe.SendResponse(callback);
                            XtraMessageBox.Show("Recorded.");
                        }
                    }
                }
                else
                {
                    XtraMessageBox.Show("You must load a door or frame to take a reading for before taking a reading.");
                }
            }
            catch (Exception ex)
            {
                MessageBox.Show("DoorRecord " + ex.Message + "-" + ex.StackTrace);
            }
        }

