                else
                {
                    start = false;
                }
                SettingsTO sto = SettingsTO.Default;
                this.btnRecordDoorLine.Enabled = sto.EnableDoorlineRecord && start;
                this.btnRecordFrameLine.Enabled = sto.EnableFramelineRecord && start;
                this.btnStart1.Enabled = (!start && COM_PORTS.Contains<string>(sto.ComPort)) && sto.EnableDoorlineRecord;
                this.btnStart2.Enabled = (!start && COM_PORTS.Contains<string>(sto.ComPort)) && sto.EnableFramelineRecord;
                this.btnStop1.Enabled = (start && COM_PORTS.Contains<string>(sto.ComPort)) && sto.EnableDoorlineRecord;
                this.btnStop2.Enabled = (start && COM_PORTS.Contains<string>(sto.ComPort)) && sto.EnableFramelineRecord;
            }
            catch (Exception ex)
            {
                MessageBox.Show("Error - " + ex.Message + " - " + ex.StackTrace);
            }
        }

        protected void DataReceived(AnalyzerRequest request)
        {
            try
            {
                base.Invoke((Action)delegate
                {
                    try
                    {
                        this.lblMessage.Text = string.Format("Request Received: {0}", DateTime.Now.ToLongTimeString());
                        this.CurrentRequest.AssemblyNumber = request.AssemblyNumber;
                        this.CurrentRequest.Description = request.Description;
                        this.CurrentRequest.Frame = request.Frame;                        
                        this.cbxFrame.EditValue = this.cbxFrame.Properties.GetKeyValueByDisplayValue(request.Frame);
                        this.CurrentRequest.IsFrame = request.IsFrame;
                        this.CurrentRequest.JobNumber = request.JobNumber;
                        this.CurrentRequest.JobQty = request.JobQty;
                        this.CurrentRequest.Line = request.Line;
                        this.cbxLine.EditValue = this.cbxLine.Properties.GetKeyValueByDisplayValue(request.Line);
                        this.CurrentRequest.Model = request.Model;
                        this.cbxModel.EditValue = this.cbxModel.Properties.GetKeyValueByDisplayValue(request.Model);
                        this.CurrentRequest.NumberOfDoors = request.NumberOfDoors;
                        this.cbxDoorCount.EditValue = this.cbxDoorCount.Properties.GetKeyValueByDisplayValue(request.NumberOfDoors);
                        this.CurrentRequest.RequestValues = request.RequestValues;
                        this.CurrentRequest.Voltage = request.Voltage;
                        this.cbxVoltage.EditValue = this.cbxVoltage.Properties.GetKeyValueByDisplayValue(request.Voltage);
                        this.lblMessage.Text = string.Format("Resetting Binding Source: {0}", DateTime.Now.ToLongTimeString());
                        this.analyzerRequestBindingSource.ResetCurrentItem();
                        this.lblMessage.Text = string.Format("Looking up power table: {0}", DateTime.Now.ToLongTimeString());
                        this.LookupPowerTableReference();
                        this.lblMessage.Text = string.Format("Setting Tab Page: {0}", DateTime.Now.ToLongTimeString());
                        if (request.IsFrame)
                        {
                            this.tabControl1.SelectedTabPage = request.RequestValues ? this.tpFrameAsm : this.tpFramePrep;
                        }
                        else
                        {
                            this.tabControl1.SelectedTabPage = request.RequestValues ? this.tpDoorAsm : this.tpDoorPrep;
                        }
                        this.lblMessage.Text = string.Format("Request recieved complete: {0}", DateTime.Now.ToLongTimeString());
                        SetForegroundWindow(Process.GetCurrentProcess().MainWindowHandle);
                    }
                    catch (Exception ex)
                    {
                        throw new Exception("DataReceived - " + ex.Message + " - " + ex.StackTrace);
                    }
                });
                if (!request.RequestValues)
                {
                    this.lblMessage.Text = string.Format("Sending Null response: {0}", DateTime.Now.ToLongTimeString());
                    this.pipe.SendResponse(null);
                }
            }
            catch (Exception ex)
            {
                throw new Exception("DataReceived - " + ex.Message + " - " + ex.StackTrace);
            }
        }

